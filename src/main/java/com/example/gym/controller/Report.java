    @FXML
    void btnsaveonaction(ActionEvent event) {

            String fromdate = String.valueOf(txtfeomdate.getValue());
            String todate = String.valueOf(txttodate.getValue());
            String income = txtreport.getText();

            var dto = new ReportDTO(fromdate,todate,income);

            try {
                boolean isSaved = reportBO.saveReport(dto);

                if (isSaved) {
                    new Alert(Alert.AlertType.CONFIRMATION, "customer saved!").show();
                    clearFields();
                }
            } catch (SQLException | ClassNotFoundException e) {
                new Alert(Alert.AlertType.ERROR, e.getMessage()).show();
            }
    }
    private void clearFields() {
        txtfeomdate.setValue(LocalDate.parse(""));
        txttodate.setValue(LocalDate.parse(""));
        txtreport.setText("");

    }

